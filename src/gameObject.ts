class gameObject {
    private width: number;
    private height: number;
    private img: string;
    private isSolid: boolean;
    private position: p5.Vector;

    constructor () {
        this.width = width;
        this.height = height;
        this.img = this.img;
        this.isSolid = true;
        this.position = position;
    }
}